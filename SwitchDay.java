class SwitchDay
{
	public static void main(String[]args)
	{
		int day=9;
		String dayString;
		switch(day){
	 		case 1:
	        	  	System.out.println(dayString="Monday");
	   	        break;
	 		case 2:
	          	  	System.out.println(dayString="Tuesday");
			break;
	 		case 3:
		 		System.out.println(dayString="Wedenesday");
	 		break;
	 		case 4:
		  		System.out.println(dayString="Thursday");
	 		break;
	 		case 5:
				System.out.println(dayString="Friday");
	 		break;
	 		case 6:
				System.out.println(dayString="Saturday");
	 		break;
	 		case 7:
				System.out.println(dayString="Sunday");
	 		break;
	  	default :
		  	System.out.println(dayString="Invaild day");
         	break;
	 	}
	}
}
		